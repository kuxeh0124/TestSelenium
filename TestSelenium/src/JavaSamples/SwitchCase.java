package JavaSamples;

public class SwitchCase {

	public static void main(String[] args) {

		int selection = 1;
		
		switch (selection){
		case 1:
			System.out.println("Selection is " + String.valueOf(selection));
			break;
		case 2:
			System.out.println("Selection is " + String.valueOf(selection));
			break;				
		}
	}

}
